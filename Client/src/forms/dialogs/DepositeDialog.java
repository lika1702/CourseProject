    public void setPartial(String partial) {
        if (partial.equals("Да")) {
            RadioButtonPartialYes.setSelected(true);
        }
        if (partial.equals("Нет")) {
            RadioButtonPartialNo.setSelected(true);
        }
    }
    
      public void setCurrency(String currency) {
        for (int i = 0; i < CurrencyCombo.getItemCount(); i++) {
            String[] mas = CurrencyCombo.getModel().getElementAt(i).split(":");
            if (mas[1].equals(currency)) {
                CurrencyCombo.setSelectedIndex(i);
            }
        }
    }

