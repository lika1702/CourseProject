    public String getPartial(){
        while (true) {
            if (RadioButtonPartialYes.isSelected()) {
                return "Да";
            }
            if (RadioButtonPartialNo.isSelected()) {
                return "Нет";
            } else {
                JOptionPane.showMessageDialog(null, "Не выбрано значение",
                        "Предупреждение", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }
    
