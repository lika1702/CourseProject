    private void DeleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteButtonActionPerformed
        String selectedValue = ListStatistics.getSelectedValue();
        String code = selectedValue.substring(0, selectedValue.indexOf(" "));
        Stream server = new Stream();
        String executeStr = "DELETE FROM Статистика WHERE `Код`=" + code;
        server.sendInt(10);
        server.sendString(executeStr);
        
        try {
            setStatisticsModel("SELECT * FROM Статистика");
            ChangeTable();
        } catch (IOException ex) {
            Logger.getLogger(MainAdminForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_DeleteButtonActionPerformed

