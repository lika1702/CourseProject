    private void ButtonStatisticsReportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonStatisticsReportActionPerformed
        ReportForm dlg = new ReportForm();
        dlg.setLocationRelativeTo(null);
        dlg.setTitle("Статистика подключеий");
        String reportText = "Отчет составлен: " + new SimpleDateFormat("dd.MM.yyyy").format(new Date()) + "\n\n";
        for(int i =0;i<ListStatistics.getModel().getSize();i++){
            String fio = ListStatistics.getModel().getElementAt(i);
            reportText += "\t" + fio + "\n";
        }
        dlg.setText(reportText);
        dlg.setVisible(true);
    }//GEN-LAST:event_ButtonStatisticsReportActionPerformed

