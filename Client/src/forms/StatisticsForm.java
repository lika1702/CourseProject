    public void setStatisticsModel(String executeStr) throws IOException {
        modelStatistics = new DefaultListModel();
        Stream server = new Stream();
        server.sendInt(17);
        server.sendString(executeStr);
        int count = server.getInt();
        for (int i = 0; i < count; i++) {
            modelStatistics.addElement(server.getString());
        }
    }

